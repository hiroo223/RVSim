.section .text

_start:
    # まず初期値をレジスタにセットする

    li x5, 12           # x5 に 12 をロード
    li x6, 7            # x6 に 7 をロード

    # 乗算（MUL命令）
    mul x7, x5, x6      # x7 = x5 * x6 = 12 * 7 = 84

    # 符号付き除算（DIV命令）
    li x8, 56           # x8 に 56 をロード
    li x9, 5            # x9 に 5 をロード
    div x10, x8, x9     # x10 = x8 / x9 = 56 / 5 = 11 (余りは無視)

    # 符号なし除算（DIVU命令）
    li x11, 56          # x11 に 56 をロード (符号なしで扱う)
    li x12, 4           # x12 に 4 をロード
    divu x13, x11, x12  # x13 = x11 / x12 = 56 / 4 = 14

    # 剰余（REM命令）
    rem x14, x8, x9     # x14 = x8 % x9 = 56 % 5 = 1

    # 符号なし剰余（REMU命令）
    remu x15, x11, x12  # x15 = x11 % x12 = 56 % 4 = 0

    # プログラム終了処理
    li a7, 10           # ecall コード 10 はプログラムの終了を表す
    ecall               # プログラム終了
